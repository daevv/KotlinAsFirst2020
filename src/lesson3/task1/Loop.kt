 */
fun revert(n: Int): Int {
    var N1 = n
    var count = 0
    while (N1 != 0) {
        N1 /= 10
        count += 1
    }
    var num = 0.0
    var N2 = n
    for (j in count - 1 downTo 0) {
        num += (N2 % 10) * 10.0.pow(j)
        N2 /= 10
    }
    return num.toInt()
}

